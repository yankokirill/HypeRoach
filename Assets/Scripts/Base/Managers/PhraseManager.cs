        public static PhraseManager Instance { get; private set; }

        private List<string> shuffledPhrases;

        // Вызвать один раз при старте игры
        public void Awake()
        {
            Instance = this;
            shuffledPhrases = new List<string>(cockroachPhrases);
            Shuffle(shuffledPhrases);
        }

        // Получать фразы по порядку из перемешанного списка
        public string GetPhrase()
        {
            return shuffledPhrases[Random.Range(0, shuffledPhrases.Count)];
        }

        // Алгоритм Фишера-Йетса
        private void Shuffle(List<string> list)
        {
            for (int i = list.Count - 1; i > 0; i--)
            {
                int j = UnityEngine.Random.Range(0, i + 1);
                string temp = list[i];
                list[i] = list[j];
                list[j] = temp;
            }
        }
